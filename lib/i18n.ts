const DICTS: Record<Locale, Dict> = {
  es,
  "es-MX": esMX,
  "es-CO": esCO,
  "es-AR": esAR,
  "pt-BR": ptBR,
  en,
};

/** Resolución con fallback: locale específico → es → clave cruda. */
export function translator(locale: Locale) {
  const base = locale.startsWith("es") ? es : {};
  const override = DICTS[locale] ?? {};
  return (key: string) => override[key] ?? base[key] ?? es[key] ?? key;
}

export type T = ReturnType<typeof translator>;
